// Hook to get current language and translate function
export const useTranslation = () => {
  const getCurrentLanguage = (): string => {
    return localStorage.getItem('userLanguage') || 'English';
  };

  const t = (key: string): string => {
    const currentLanguage = getCurrentLanguage();
    return translations[key]?.[currentLanguage] || translations[key]?.['English'] || key;
  };

  return {
    t,
    currentLanguage: getCurrentLanguage()
  };
};

// Function to trigger re-render when language changes
export const createLanguageChangeEvent = () => {
  window.dispatchEvent(new CustomEvent('languageChanged'));
};